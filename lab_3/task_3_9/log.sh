#!/bin/bash

# Проверка существования файла
LOG_FILE="system_report.log"

echo "=== Проверка лог-файла: $LOG_FILE ==="

# Использую флаг -f, чтобы проверить, существует ли файл и является ли он обычным файлом
if [ -f "$LOG_FILE" ]; then
    echo "[OK] Файл '$LOG_FILE' существует."

    # Анализ "кода ошибки"
    # Представлю, что в переменной ERROR_CODE хранится некий результат (0 - успех, 1 - ошибка)
    # В реальном скрипте это значение могло бы прийти из другой команды или файла.
    ERROR_CODE=0  # Попробую изменить на 0, чтобы увидеть другую ветку

    echo "Анализ кода ошибки (значение: $ERROR_CODE):"

    # Сравниваю число. Важно: для чисел используем -eq, а не ==
    if [ "$ERROR_CODE" -eq 0 ]; then
        echo "  Статус: Ошибок не обнаружено. Работа завершена успешно."
    else
        echo "  Статус: ОБНАРУЖЕН КРИТИЧЕСКИЙ СБОЙ! Код ошибки: $ERROR_CODE"
        echo "  Рекомендация: Проверьте файл '$LOG_FILE' на наличие проблем."
    fi

else
    echo "[ОШИБКА] Файл '$LOG_FILE' не найден в текущей директории ($PWD)."
    echo "Пожалуйста, создайте этот файл или укажите правильное имя."
fi

echo "=== Проверка завершена === 
