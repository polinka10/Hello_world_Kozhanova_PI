#!/bin/bash
echo "Запуск числового фильтра (числа от 1 до 20):"

for i in {1..20}; do
    # Если число равно 15, прерываю цикл
    if [ $i -eq 15 ]; then
        echo "  Достигнуто число 15. Останавливаю цикл."
        break
    fi

    # Проверка на чётность (остаток от деления на 2 равен 0)
    if [ $((i % 2)) -eq 0 ]; then
        # Пропускаю чётное число
        continue
    fi

    # Если дошёл до сюда, значит число нечётное и не 15
    echo "  Нечётное число: $i"
done

echo "Работа скрипта завершена."
