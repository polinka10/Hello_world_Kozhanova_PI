# Запрашиваем группу крови текстом
blood_type = input("Введите вашу группу крови (A, B, AB или 0): ").upper()

# Анализируем введённый текст
if blood_type == "A":
    print("Можно получать от групп A и 0")
elif blood_type == "B":
    print("Можно получать от групп B и 0")
elif blood_type == "AB":
    print("Универсальный реципиент — можно от всех групп")
elif blood_type == "0":
    print("Универсальный донор, но получать можно только от 0")
else:
    print("Неизвестная группа крови")